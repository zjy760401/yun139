const fs = require('fs');
const path = require('path');
const infile = '/tmp/mcloud_api_results.json';
if (!fs.existsSync(infile)) { console.error('input not found'); process.exit(2); }
const arr = JSON.parse(fs.readFileSync(infile,'utf8'));
const byPath = new Map();
for (const e of arr) {
  const key = e.request || '(variable_request)';
  if (!byPath.has(key)) byPath.set(key, []);
  byPath.get(key).push(e);
}

function camelToWords(name){
  if(!name) return '';
  // split camelCase and underscores
  return name.replace(/([a-z])([A-Z])/g,'$1 $2').replace(/[_\-]/g,' ').replace(/([A-Z])([A-Z][a-z])/g,'$1 $2');
}

function guessDesc(key, items){
  // attempt to use function names
  const names = items.map(i=>i.funcName).filter(Boolean);
  if(names.length){
    const name = names[0];
    const words = camelToWords(name);
    // simple verbs mapping
    if(/get/i.test(name) || /query|list|fetch/i.test(name)) return `Retrieve ${words}`;
    if(/create|add|upload|commit|post/i.test(name)) return `Create/Upload ${words}`;
    if(/delete|del|remove/i.test(name)) return `Delete ${words}`;
    if(/update|modify|edit/i.test(name)) return `Update ${words}`;
    if(/login|auth|token|refresh|logout/i.test(name)) return `Authentication related: ${words}`;
    return `${words}`;
  }
  // fallback use path
  let p = key;
  if(p.includes('/')){
    const parts = p.split('/').filter(Boolean);
    if(parts.length) return `API for ${parts.join(' / ')}`;
  }
  return 'No description available';
}

let md = [];
md.push('# Extracted API Document for 中国移动云盘 (partial, auto-generated)');
md.push('Generated by static analysis of app.asar. Descriptions are heuristic guesses.');
md.push('\n## Global notes');
md.push('- Auth: Utils.sendRequest adds header `Authorization: Basic ` + btoa("pc:"+window.UserInfo.account+":"+window.UserInfo.authToken) when UserInfo.authToken exists.');
md.push('- Content-Type: defaults to application/json;charset=utf-8 when sending JSON, or application/xml for xml requests; Utils may convert data to XML for non-json options.');
md.push('- Additional headers: options.appendHeads can add custom headers like `x-yun-sbox-session-id` or `x-yun-url-type`.');
md.push('- Method: heuristically determined — many calls use POST when data argument is present.');
md.push('\n---\n');

const entries = Array.from(byPath.entries()).sort((a,b)=>a[0].localeCompare(b[0]));
for (const [key, items] of entries) {
  md.push(`### ${key}`);
  const methods = new Set(items.map(i=>i.methodGuess));
  const funcs = [...new Set(items.map(i=>i.funcName).filter(Boolean))];
  const cats = [...new Set(items.flatMap(i=>i.categories||[]).filter(Boolean))];
  const files = [...new Set(items.map(i=>i.file))];
  md.push(`- Method (guessed): ${[...methods].join(', ')}`);
  md.push(`- Called from functions: ${funcs.slice(0,5).join(', ') || 'N/A'}`);
  if (cats.length) md.push(`- Categories: ${cats.join(' > ')}`);
  md.push(`- Files: ${files.slice(0,5).map(f=>f.replace('/tmp/mcloud_asar_extract/','')).join(', ')}`);
  md.push(`- Sample call snippet: 


\n`);
  const sample = items.find(i=>i.argsSnippet) || items[0];
  md.push('```javascript');
  md.push(`// file: ${sample.file.replace('/tmp/mcloud_asar_extract/','')}, line: ${sample.line}`);
  md.push(`// func: ${sample.funcName || 'unknown'}`);
  md.push(sample.argsSnippet.replace(/\n/g,'\n'));
  md.push('```');
  md.push(`- Description (heuristic): ${guessDesc(key, items)}`);
  md.push('\n');
}

fs.writeFileSync('/tmp/mcloud_api_doc.md', md.join('\n\n'));
console.log('WROTE /tmp/mcloud_api_doc.md with', entries.length, 'endpoints');
